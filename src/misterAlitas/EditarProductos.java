    private void botonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonGuardarActionPerformed
        Producto p = new Producto();
        String ids = this.campoId.getText();
        String nombre = this.campoNombre.getText();
        String codigo = this.campoCodigo.getText();
        String descripcion = this.campoDescripcion.getText();
        String precio = this.campoPrecio.getText();
        String restricciones = (String) this.campoRestricciones.getSelectedItem();
        String cantidad = this.campoCantidad.getText();

        if (nombre.isEmpty() || codigo.isEmpty() || descripcion.isEmpty() || precio.isEmpty() || cantidad.isEmpty()) {

            JOptionPane.showMessageDialog(null, "Uno de los campos está vacio, por favor llene todos los campos", "Validacion", JOptionPane.ERROR_MESSAGE);
        } else {

            int a = Integer.parseInt(codigo);
            int b = Integer.parseInt(precio);
            int c = Integer.parseInt(cantidad);
            int d = Integer.parseInt(ids);
            p.setIdproducto(d);
            p.setNombreProducto(nombre);
            p.setCodigo(a);
            p.setDescripcion(descripcion);
            p.setPrecio(b);
            p.setRestricciones(restricciones);
            p.setCantidad(c);
            
            editarProducto(p); 
            
        }
        
        if (bandera == true) {
            JOptionPane.showMessageDialog(null, "Se ha registrado el producto con éxito", "Aviso", JOptionPane.INFORMATION_MESSAGE);
            //Revisar que hace al terminar de registrar un producto.
            campoNombre.setText("");
            campoCodigo.setText("");
            campoDescripcion.setText("");
            campoPrecio.setText("");
            campoRestricciones.setSelectedIndex(0);
            campoCantidad.setText("");
            
            
        } else {
            JOptionPane.showMessageDialog(null, "Producto NO registrado", "Validacion", JOptionPane.ERROR_MESSAGE);
        }


    }//GEN-LAST:event_botonGuardarActionPerformed

    private void campoCodigoKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_campoCodigoKeyTyped
        char c=evt.getKeyChar();
        if((c<'0'||c>'9')&&(c!=(char)KeyEvent.VK_BACK_SPACE)){
            evt.consume();
            JOptionPane.showMessageDialog(null, "Sólo se permiten números", "Validar número", JOptionPane.INFORMATION_MESSAGE);
        }
        String Caracteres = campoCodigo.getText();
        if(Caracteres.length()>=10){
            evt.consume();
            
             getToolkit().beep();
        }
    }//GEN-LAST:event_campoCodigoKeyTyped

