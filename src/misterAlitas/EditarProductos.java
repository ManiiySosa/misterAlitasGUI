    //Método para editar un elemento
    public void editarProducto(Producto productoOriginal) {
        EntityManagerFactory emf = Persistence.createEntityManagerFactory("default");
        EntityManager em = emf.createEntityManager();
        try {
            
            Producto productoAux = em.find(Producto.class, productoOriginal.getIdproducto());
            if (productoAux != null) {
                em.getTransaction().begin();
                productoAux.setNombreProducto(productoOriginal.getNombreProducto());
                productoAux.setCodigo(productoOriginal.getCodigo());
                productoAux.setDescripcion(productoOriginal.getDescripcion());
                productoAux.setPrecio(productoOriginal.getPrecio());
                productoAux.setRestricciones(productoOriginal.getRestricciones());
                productoAux.setCantidad(productoOriginal.getCantidad());
            
           
           
                em.getTransaction().commit();
                JOptionPane.showMessageDialog(null, "Edición realizada con éxito ;D", "Mensaje", JOptionPane.INFORMATION_MESSAGE);
                
            } else {
                JOptionPane.showMessageDialog(null, "No se encontró el usuario", "Error", JOptionPane.ERROR_MESSAGE);
            }
            
        } catch (Exception e) {
            e.printStackTrace();
            em.getTransaction().rollback();
            new MenuPrincipal().setVisible(true);
            
        } finally {
            em.close();
        }
        
    }

